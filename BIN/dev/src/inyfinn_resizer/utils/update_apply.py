"""Generowanie skryptu instalacji aktualizacji (PowerShell)."""

from __future__ import annotations

from pathlib import Path

from inyfinn_resizer.utils.install_layout import install_layout


def _ps_literal(path: Path) -> str:
    return str(path.resolve()).replace("'", "''")


def write_apply_script(
    *,
    zip_path: Path,
    install_root: Path,
    version: str,
    launcher: Path,
    package_dir: Path,
) -> Path:
    layout = install_layout()
    script_dir = package_dir
    script_dir.mkdir(parents=True, exist_ok=True)
    script_path = script_dir / "apply_update.ps1"

    ps1 = f"""# Auto-generated by Inyfinn Photo Resizer
$ErrorActionPreference = "Stop"
$ZipPath = '{_ps_literal(zip_path)}'
$InstallRoot = '{_ps_literal(install_root)}'
$Version = '{version.replace("'", "''")}'
$Launcher = '{_ps_literal(launcher)}'
$PackageDir = '{_ps_literal(package_dir)}'
$Layout = '{layout}'

$deadline = (Get-Date).AddMinutes(8)
while ((Get-Process InyfinnPhotoResizer -ErrorAction SilentlyContinue) -and ((Get-Date) -lt $deadline)) {{
    Start-Sleep -Seconds 1
}}

$tempExtract = Join-Path $env:TEMP ("inyfinn-update-" + $Version)
if (Test-Path $tempExtract) {{ Remove-Item $tempExtract -Recurse -Force }}
New-Item -ItemType Directory -Force -Path $tempExtract | Out-Null
Expand-Archive -LiteralPath $ZipPath -DestinationPath $tempExtract -Force

if ($Layout -eq "portable") {{
    $srcBin = Join-Path $tempExtract "BIN"
    $dstBin = Join-Path $InstallRoot "BIN"
    if (-not (Test-Path $srcBin)) {{ throw "Brak BIN w pakiecie aktualizacji" }}
    New-Item -ItemType Directory -Force -Path $dstBin | Out-Null
    robocopy $srcBin $dstBin /E /IS /IT /NFL /NDL /NJH /NJS /nc /ns /np | Out-Null
    if ($LASTEXITCODE -ge 8) {{ throw "robocopy BIN failed: $LASTEXITCODE" }}
    $rootExe = Join-Path $tempExtract "InyfinnPhotoResizer.exe"
    if (Test-Path $rootExe) {{
        Copy-Item -LiteralPath $rootExe -Destination (Join-Path $InstallRoot "InyfinnPhotoResizer.exe") -Force
    }}
    $rootIco = Join-Path $tempExtract "InyfinnPhotoResizer.ico"
    if (Test-Path $rootIco) {{
        Copy-Item -LiteralPath $rootIco -Destination (Join-Path $InstallRoot "InyfinnPhotoResizer.ico") -Force
    }}
}} else {{
    $srcBin = Join-Path $tempExtract "BIN"
    if (Test-Path $srcBin) {{
        $appExe = Join-Path $srcBin "InyfinnPhotoResizer.exe"
        if (Test-Path $appExe) {{
            Copy-Item -LiteralPath $appExe -Destination (Join-Path $InstallRoot "InyfinnPhotoResizer.exe") -Force
        }}
        $internalSrc = Join-Path $srcBin "_internal"
        $internalDst = Join-Path $InstallRoot "_internal"
        if (Test-Path $internalSrc) {{
            New-Item -ItemType Directory -Force -Path $internalDst | Out-Null
            robocopy $internalSrc $internalDst /E /IS /IT /NFL /NDL /NJH /NJS /nc /ns /np | Out-Null
            if ($LASTEXITCODE -ge 8) {{ throw "robocopy _internal failed: $LASTEXITCODE" }}
        }}
    }} else {{
        throw "Nieobsługiwany układ pakietu aktualizacji"
    }}
}}

Remove-Item -LiteralPath $tempExtract -Recurse -Force -ErrorAction SilentlyContinue
Remove-Item -LiteralPath $PackageDir -Recurse -Force -ErrorAction SilentlyContinue

Start-Process -FilePath $Launcher
"""
    script_path.write_text(ps1, encoding="utf-8")
    return script_path

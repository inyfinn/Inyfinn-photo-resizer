"""Generowanie skryptu instalacji aktualizacji (PowerShell + okno postępu)."""

from __future__ import annotations

import os
import tempfile
from pathlib import Path

from inyfinn_resizer.utils.install_layout import install_layout


def _ps_literal(path: Path) -> str:
    return str(path.resolve()).replace("'", "''")


def _temp_dir() -> Path:
    """Katalog %TEMP% — wyłącznie przez tempfile / zmienne środowiskowe OS."""
    try:
        return Path(tempfile.gettempdir())
    except Exception:
        for key in ("TEMP", "TMP", "LOCALAPPDATA"):
            raw = os.environ.get(key)
            if raw:
                candidate = Path(raw) if key != "LOCALAPPDATA" else Path(raw) / "Temp"
                if candidate.is_dir():
                    return candidate
        return Path.cwd()


def write_apply_script(
    *,
    zip_path: Path,
    install_root: Path,
    version: str,
    launcher: Path,
    package_dir: Path,
) -> Path:
    layout = install_layout()
    package_dir.mkdir(parents=True, exist_ok=True)

    log_path = package_dir.parent / "update-install.log"
    script_body = _build_script_body(
        zip_path=zip_path,
        install_root=install_root,
        version=version,
        launcher=launcher,
        package_dir=package_dir,
        log_path=log_path,
        layout=layout,
    )

    # Skrypt w %TEMP% — nie kasuje się razem z folderem pakietu.
    temp_script = _temp_dir() / f"inyfinn-apply-update-{version}.ps1"
    # utf-8-sig = UTF-8 z BOM; PowerShell 5.1 rozpoznaje BOM i odczytuje poprawnie
    temp_script.write_text(script_body, encoding="utf-8-sig")
    return temp_script


def _build_script_body(
    *,
    zip_path: Path,
    install_root: Path,
    version: str,
    launcher: Path,
    package_dir: Path,
    log_path: Path,
    layout: str,
) -> str:
    return f"""# Auto-generated by Inyfinn Photo Resizer
$ErrorActionPreference = "Stop"
Add-Type -AssemblyName System.Windows.Forms
Add-Type -AssemblyName System.Drawing

$ZipPath = '{_ps_literal(zip_path)}'
$InstallRoot = '{_ps_literal(install_root)}'
$Version = '{version.replace("'", "''")}'
$Launcher = '{_ps_literal(launcher)}'
$PackageDir = '{_ps_literal(package_dir)}'
$LogPath = '{_ps_literal(log_path)}'
$Layout = '{layout}'

function Write-Log {{
    param([string]$Message)
    $line = "[$(Get-Date -Format 'yyyy-MM-dd HH:mm:ss')] $Message"
    try {{
        Add-Content -LiteralPath $LogPath -Value $line -Encoding UTF8
    }} catch {{}}
}}

function Update-Ui {{
    param([string]$Status, [int]$Percent = -1)
    if ($script:StatusLabel) {{
        $script:StatusLabel.Text = $Status
    }}
    if ($script:ProgressBar -and $Percent -ge 0) {{
        $script:ProgressBar.Value = [Math]::Min(100, [Math]::Max(0, $Percent))
    }}
    [System.Windows.Forms.Application]::DoEvents()
}}

function Show-InstallError {{
    param([string]$Message)
    Write-Log "ERROR: $Message"
    [System.Windows.Forms.MessageBox]::Show(
        $Message,
        "Aktualizacja Inyfinn Photo Resizer",
        [System.Windows.Forms.MessageBoxButtons]::OK,
        [System.Windows.Forms.MessageBoxIcon]::Error
    ) | Out-Null
}}

$form = New-Object System.Windows.Forms.Form
$form.Text = "Aktualizacja Inyfinn Photo Resizer"
$form.Size = New-Object System.Drawing.Size(480, 170)
$form.StartPosition = "CenterScreen"
$form.FormBorderStyle = "FixedDialog"
$form.MaximizeBox = $false
$form.MinimizeBox = $false
$form.TopMost = $true

$title = New-Object System.Windows.Forms.Label
$title.Location = New-Object System.Drawing.Point(16, 14)
$title.Size = New-Object System.Drawing.Size(440, 22)
$title.Font = New-Object System.Drawing.Font("Segoe UI", 10, [System.Drawing.FontStyle]::Bold)
$title.Text = "Instalacja wersji $Version"
$form.Controls.Add($title)

$script:StatusLabel = New-Object System.Windows.Forms.Label
$script:StatusLabel.Location = New-Object System.Drawing.Point(16, 42)
$script:StatusLabel.Size = New-Object System.Drawing.Size(440, 36)
$script:StatusLabel.Text = "Zamykanie aplikacji…"
$form.Controls.Add($script:StatusLabel)

$script:ProgressBar = New-Object System.Windows.Forms.ProgressBar
$script:ProgressBar.Location = New-Object System.Drawing.Point(16, 84)
$script:ProgressBar.Size = New-Object System.Drawing.Size(440, 22)
$script:ProgressBar.Style = "Continuous"
$script:ProgressBar.Minimum = 0
$script:ProgressBar.Maximum = 100
$script:ProgressBar.Value = 0
$form.Controls.Add($script:ProgressBar)

$form.Add_Shown({{ $form.Activate() }})
$form.Show()
Update-Ui "Przygotowanie aktualizacji do wersji $Version…" 5

try {{
    Write-Log "Start instalacji v$Version do $InstallRoot (layout=$Layout)"

    Update-Ui "Oczekiwanie na zamknięcie aplikacji…" 10
    $deadline = (Get-Date).AddMinutes(8)
    while ((Get-Process -Name "InyfinnPhotoResizer" -ErrorAction SilentlyContinue) -and ((Get-Date) -lt $deadline)) {{
        Start-Sleep -Milliseconds 500
        [System.Windows.Forms.Application]::DoEvents()
    }}
    if (Get-Process -Name "InyfinnPhotoResizer" -ErrorAction SilentlyContinue) {{
        Write-Log "Wymuszanie zamknięcia procesu"
        Stop-Process -Name "InyfinnPhotoResizer" -Force -ErrorAction SilentlyContinue
        Start-Sleep -Seconds 2
    }}

    Update-Ui "Rozpakowywanie pakietu aktualizacji…" 25
    $tempExtract = Join-Path $env:TEMP ("inyfinn-update-" + $Version)
    if (Test-Path $tempExtract) {{ Remove-Item $tempExtract -Recurse -Force }}
    New-Item -ItemType Directory -Force -Path $tempExtract | Out-Null
    Expand-Archive -LiteralPath $ZipPath -DestinationPath $tempExtract -Force
    Write-Log "Rozpakowano do $tempExtract"

    function Invoke-RobocopyRetry {{
        param([string]$Source, [string]$Destination)
        New-Item -ItemType Directory -Force -Path $Destination | Out-Null
        for ($attempt = 1; $attempt -le 5; $attempt++) {{
            robocopy $Source $Destination /E /IS /IT /NFL /NDL /NJH /NJS /nc /ns /np | Out-Null
            if ($LASTEXITCODE -lt 8) {{
                return
            }}
            Write-Log "robocopy próba $attempt nieudana (kod $LASTEXITCODE), ponawiam…"
            Start-Sleep -Seconds 2
        }}
        throw "Nie udało się skopiować plików (robocopy kod $LASTEXITCODE)"
    }}

    if ($Layout -eq "portable") {{
        Update-Ui "Kopiowanie plików aplikacji…" 55
        $srcBin = Join-Path $tempExtract "BIN"
        $dstBin = Join-Path $InstallRoot "BIN"
        if (-not (Test-Path $srcBin)) {{ throw "Brak folderu BIN w pakiecie aktualizacji" }}
        Invoke-RobocopyRetry -Source $srcBin -Destination $dstBin
        Write-Log "Skopiowano BIN"

        Update-Ui "Aktualizacja launchera…" 80
        $rootExe = Join-Path $tempExtract "InyfinnPhotoResizer.exe"
        if (Test-Path $rootExe) {{
            Copy-Item -LiteralPath $rootExe -Destination (Join-Path $InstallRoot "InyfinnPhotoResizer.exe") -Force
        }}
        $rootIco = Join-Path $tempExtract "InyfinnPhotoResizer.ico"
        if (Test-Path $rootIco) {{
            Copy-Item -LiteralPath $rootIco -Destination (Join-Path $InstallRoot "InyfinnPhotoResizer.ico") -Force
        }}
    }} elseif ($Layout -eq "flat") {{
        Update-Ui "Kopiowanie plików aplikacji…" 55
        $srcBin = Join-Path $tempExtract "BIN"
        if (-not (Test-Path $srcBin)) {{ throw "Brak folderu BIN w pakiecie aktualizacji" }}
        $appExe = Join-Path $srcBin "InyfinnPhotoResizer.exe"
        if (Test-Path $appExe) {{
            Copy-Item -LiteralPath $appExe -Destination (Join-Path $InstallRoot "InyfinnPhotoResizer.exe") -Force
        }}
        $internalSrc = Join-Path $srcBin "_internal"
        $internalDst = Join-Path $InstallRoot "_internal"
        if (Test-Path $internalSrc) {{
            Invoke-RobocopyRetry -Source $internalSrc -Destination $internalDst
        }}
        Write-Log "Skopiowano układ flat"
    }} else {{
        throw "Nieobsługiwany układ instalacji: $Layout"
    }}

    Update-Ui "Czyszczenie plików tymczasowych…" 92
    Remove-Item -LiteralPath $tempExtract -Recurse -Force -ErrorAction SilentlyContinue
    Remove-Item -LiteralPath $PackageDir -Recurse -Force -ErrorAction SilentlyContinue

    Update-Ui "Uruchamianie zaktualizowanej aplikacji…" 100
    Write-Log "Instalacja zakończona, start: $Launcher"
    Start-Sleep -Milliseconds 400
    Start-Process -FilePath $Launcher
}}
catch {{
    Show-InstallError ("Aktualizacja nie powiodła się:`n`n" + $_.Exception.Message)
    exit 1
}}
finally {{
    if ($form.Visible) {{
        $form.Close()
    }}
}}
"""
